def render_reference(manifest: dict[str, Any]) -> str:
    lines = [
        HEADER_MD.rstrip(),
        "## Tau Python API",
        "",
        "The `py_repl` namespace exposes a persistent `tau` object for programmatic orchestration.",
        "",
        "### Methods",
        "",
    ]
    for method in manifest["methods"]:
        lines.extend(
            [
                f"- `tau.{method['signature'].replace('self, ', '').replace('(self)', '()')}` -> `{method['return']}`",
                f"  {method['summary']}",
            ]
        )
    lines.extend(["", "### Parallel Specs", ""])
    for factory in manifest["factories"]:
        lines.append(f"- `tau.{factory['signature']}`: {factory['summary']}")
    lines.extend(["", "### Result Types", ""])
    for result in manifest["result_types"]:
        fields = ", ".join(field["name"] for field in result["fields"])
        lines.append(f"- `{result['name']}` fields: {fields}")
    lines.extend(["", "### Environment", ""])
    for env in manifest["environment"]:
        lines.append(f"- `tau.{env['name']}`: {env['description']}")
    lines.extend(["", "### Examples", ""])
    lines.append("```python")
    for method in manifest["methods"]:
        lines.append(method["example"])
    lines.append("```")
    return "\n".join(lines) + "\n"


def render_description(manifest: dict[str, Any]) -> str:
    return textwrap.fill(manifest["description"], width=100) + "\n"


def render_guideline(manifest: dict[str, Any]) -> str:
    return textwrap.fill(manifest["guideline"], width=100) + "\n"


def generated_outputs(manifest: dict[str, Any]) -> dict[pathlib.Path, str]:
    return {
        KERNEL_PATH: render_kernel(manifest),
        REFERENCE_PATH: render_reference(manifest),
        DESCRIPTION_PATH: render_description(manifest),
        GUIDELINE_PATH: render_guideline(manifest),
    }


def check_or_write(outputs: dict[pathlib.Path, str], check: bool) -> int:
    failures = 0
    for path, content in outputs.items():
        if check:
            existing = path.read_text() if path.exists() else ""
            if existing != content:
                failures += 1
                diff = difflib.unified_diff(
                    existing.splitlines(keepends=True),
                    content.splitlines(keepends=True),
                    fromfile=str(path),
                    tofile=f"{path} (generated)",
                )
                sys.stderr.writelines(diff)
        else:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(content)
    return failures


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true", help="fail if generated files are stale")
    args = parser.parse_args()
    manifest = load_manifest()
    failures = check_or_write(generated_outputs(manifest), args.check)
    if failures:
        print(f"{failures} generated file(s) are stale; run scripts/generate-py-tau-api.py", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
