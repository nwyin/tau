def py_literal(value: Any) -> str:
    return repr(value)


def load_manifest() -> dict[str, Any]:
    with MANIFEST_PATH.open() as f:
        return json.load(f)


def render_result_type(result: dict[str, Any]) -> str:
    lines: list[str] = [
        f"class {result['name']}:",
        f'    """{result["doc"]}"""',
        "",
        "    def __init__(self, data):",
    ]
    fallback = result.get("string_fallback")
    if fallback:
        lines.append("        if isinstance(data, str):")
        for name, value in fallback.items():
            expr = "data" if value == "$data" else py_literal(value)
            lines.append(f"            self.{name} = {expr}")
        lines.append("        else:")
        field_indent = " " * 12
    else:
        field_indent = " " * 8
    for field in result["fields"]:
        default = py_literal(field.get("default"))
        lines.append(f'{field_indent}self.{field["name"]} = data.get("{field["name"]}", {default})')

    for prop in result.get("properties", []):
        lines.extend(
            [
                "",
                "    @property",
                f"    def {prop['name']}(self):",
                f"        return {prop['expression']}",
            ]
        )

    lines.extend(
        [
            "",
            "    def __str__(self):",
            f"        return {result['str']}",
            "",
            "    def __repr__(self):",
            f"        return {result['repr']}",
            "",
            "    def __bool__(self):",
            f"        return {result['bool']}",
        ]
    )
    if "contains" in result:
        lines.extend(
            [
                "",
                "    def __contains__(self, item):",
                f"        return {result['contains']}",
            ]
        )
    return "\n".join(lines)


def render_thread_params(manifest: dict[str, Any]) -> str:
    params = [p["name"] for p in manifest["thread_params"]]
    signature = "\n".join(f"        {name}," for name in params)
    lines = [
        "    def _thread_params(",
        "        self,",
        signature,
        "    ):",
        '        params = {"alias": alias, "task": task}',
    ]
    for name in params[2:]:
        lines.extend(
            [
                f"        if {name} is not None:",
                f'            params["{name}"] = {name}',
            ]
        )
    lines.append("        return params")
    return "\n".join(lines)


def render_method(method: dict[str, Any]) -> str:
    lines = [f"    def {method['signature']}:", f'        """{method["summary"]}"""']
    kind = method["kind"]
    rpc = method["rpc"]
    if kind == "tool":
        lines.append(f'        return self._rpc("{rpc}", {{"name": name, "args": kwargs}})')
    elif kind == "thread":
        wrapper = method["wrapper"]
        names = ["alias", "task", "tools", "model", "episodes", "timeout", "max_turns", "worktree", "worktree_base", "worktree_include"]
        lines.append("        params = self._thread_params(")
        for name in names:
            lines.append(f"            {name},")
        lines.append("        )")
        lines.append(f'        return {wrapper}(self._rpc("{rpc}", params))')
    elif kind == "poll":
        wrapper = method["wrapper"]
        lines.append(f'        return {wrapper}(self._rpc("{rpc}", {{"alias": self._coerce_alias(alias)}}))')
    elif kind == "wait":
        lines.extend(
            [
                "        normalized = [self._coerce_alias(alias) for alias in aliases]",
                '        params = {"aliases": normalized}',
                "        if timeout is not None:",
                '            params["timeout"] = timeout',
                f'        return [ThreadResult(item) for item in self._rpc("{rpc}", params)]',
            ]
        )
    elif kind == "query":
        lines.extend(
            [
                '        params = {"prompt": prompt}',
                "        if alias is not None:",
                '            params["alias"] = alias',
                "        if model is not None:",
                '            params["model"] = model',
                f'        return self._rpc("{rpc}", params)',
            ]
        )
    elif kind == "parallel":
        lines.extend(
            [
                f'        raw = self._rpc("{rpc}", {{"specs": list(specs)}})',
                "        specs_list = list(specs)",
                "        wrapped = []",
                "        for i, val in enumerate(raw):",
                '            if i < len(specs_list) and specs_list[i].get("method") == "thread":',
                "                wrapped.append(ThreadResult(val))",
                "            else:",
                "                wrapped.append(val)",
                "        return wrapped",
            ]
        )
    elif kind == "document":
        lines.extend(
            [
                '        params = {"operation": operation}',
                "        if name is not None:",
                '            params["name"] = name',
                "        if content is not None:",
                '            params["content"] = content',
                f'        return self._rpc("{rpc}", params)',
            ]
        )
    elif kind == "log":
        lines.append(f'        self._rpc("{rpc}", {{"message": str(message)}})')
    elif kind == "alias_result":
        wrapper = method["wrapper"]
        lines.append(f'        return {wrapper}(self._rpc("{rpc}", {{"alias": alias}}))')
    elif kind == "no_params":
        lines.append(f'        return self._rpc("{rpc}", {{}})')
    else:
        raise ValueError(f"unknown method kind: {kind}")
    return "\n".join(lines)


def render_factory(factory: dict[str, Any]) -> str:
    lines = [
        "    @staticmethod",
        f"    def {factory['signature']}:",
        f'        """{factory["summary"]}"""',
    ]
    fields = ", ".join(f'"{field}": {field}' for field in factory["fields"])
    if factory.get("args_field"):
        lines.append(f'        return {{"method": "{factory["method"]}", {fields}, "{factory["args_field"]}": kwargs}}')
    else:
        lines.append(f'        return {{"method": "{factory["method"]}", {fields}, **kwargs}}')
    return "\n".join(lines)


